
package holamundo;

public class HolaMundo {

    public static void main(String[] args) {
        byte num1=12;
        short num2=13;
        int num3=14;
        long num4=15;
        float num5=21.6f;
        double num6=0.21;
        char caracter='a';
        boolean desicion=true;
        
        System.out.println("Tipo byte: "+num1);
        System.out.println("Tipo short: "+num2);
        System.out.println("Tipo int: "+num3);
        System.out.println("Tipo long: "+num4);
        System.out.println("Tipo float: "+num5);
        System.out.println("Tipo double: "+num6);
        System.out.println("Tipo char: "+caracter);
        System.out.println("Tipo boolen: "+desicion);
        
    }
    
}
